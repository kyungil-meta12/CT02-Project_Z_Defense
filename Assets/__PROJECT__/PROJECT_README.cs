 * * ==========================================================================================
 * 1. CORE SYSTEM & GAMEPLAY (핵심 기획 의도 및 차별점)
 * ==========================================================================================
 * - [요새 방어] 요새 내부를 배회하는 아군 NPC를 선택하여 타워에 배치 및 타워 업그레이드 진행.
 * - [웨이브 시스템] 각 Wave마다 몰려오는 좀비를 처치하며, 특정 Wave에는 특수 보스 좀비 출현.
