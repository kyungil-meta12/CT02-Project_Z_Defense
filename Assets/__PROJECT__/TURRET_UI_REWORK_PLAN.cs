/* ==========================================================================================
 * [TURRET UI REWORK PLAN] 터렛 선택/업그레이드/상세정보/스킬 UI 재설계 계획
 * ==========================================================================================
 *
 * 목적
 * ------------------------------------------------------------------------------------------
 * 기존 터렛 업그레이드 팝업은 업그레이드, 진화, 상세 스탯, 엔지니어 정보 등 너무 많은
 * 정보를 한 화면에 담으려 해서 유지보수와 UI 가독성이 떨어졌다.
 *
 * 앞으로는 터렛 선택 팝업을 작은 허브로 두고, 실제 기능은 전용 팝업으로 분리한다.
 *
 * ==========================================================================================
 * 1. 기본 선택 흐름
 * ==========================================================================================
 *
 * 1. 터렛 1번 클릭
 *    - 팝업은 열지 않는다.
 *    - 선택된 터렛의 사거리만 표시한다.
 *
 * 2. 같은 터렛을 1초 안에 다시 클릭
 *    - TurretSelectPopup을 표시한다.
 *    - 사거리 표시는 유지한다.
 *
 * 3. 선택 해제
 *    - TurretSelectPopup의 X 버튼을 누르면 닫는다.
 *    - TurretSelectPopup 밖 영역을 클릭해도 닫는다.
 *    - 닫을 때 터렛 선택과 사거리 표시도 함께 해제하는 것을 기본 정책으로 한다.
 *
 * ==========================================================================================
 * 2. TurretSelectPopup 역할
 * ==========================================================================================
 *
 * TurretSelectPopup은 터렛의 모든 정보를 보여주는 화면이 아니다.
 * 선택된 터렛에 대해 다음 행동을 고르는 허브 UI로만 사용한다.
 *
 * 필수 버튼
 * ------------------------------------------------------------------------------------------
 * 1. 업그레이드
 *    - TurretUpgradePopup을 연다.
 *
 * 2. 세부정보
 *    - TurretDetailPopup을 연다.
 *
 * 3. 스킬
 *    - TurretSkillPopup을 연다.
 *    - 스킬 시스템이 준비되지 않았다면 비활성 또는 "준비 중" 상태로 둔다.
 *
 * 4. 닫기
 *    - 우측 상단 작은 X 버튼으로 제공한다.
 *
 * 권장 표시 정보
 * ------------------------------------------------------------------------------------------
 * - 터렛 이름
 * - 현재 레벨
 * - 현재 진화/등급 이름 또는 짧은 상태 문구
 *
 * 넣지 않을 정보
 * ------------------------------------------------------------------------------------------
 * - 전체 스탯 목록
 * - 업그레이드 수치 변경 상세
 * - 엔지니어 상세 정보
 * - 스킬 상세 설명
 *
 * ==========================================================================================
 * 3. TurretUpgradePopup 역할
 * ==========================================================================================
 *
 * TurretUpgradePopup은 업그레이드와 진화만 담당한다.
 *
 * 표시 정보
 * ------------------------------------------------------------------------------------------
 * - 현재 티어 레벨
 * - 다음 티어 레벨
 * - 업그레이드 비용
 * - 업그레이드 시 변경되는 핵심 수치
 *   예: 공격력, 사거리, 발사 간격 등 실제로 변하는 값 위주
 *
 * 버튼
 * ------------------------------------------------------------------------------------------
 * 1. 업그레이드
 *    - 비용과 조건을 만족하면 현재 티어 레벨을 올린다.
 *    - 업그레이드 후 수치 변경점을 즉시 다시 계산해 표시한다.
 *
 * 2. 진화
 *    - 진화 요구 레벨 전에는 비활성 상태로 둔다.
 *    - 표시 문구 예: "Lv.100 필요"
 *    - 현재 티어 레벨이 요구 레벨에 도달하고 진화 후보가 있으면 활성화한다.
 *    - 진화 후보가 여러 개면 별도의 진화 선택 영역 또는 진화 선택 팝업을 사용한다.
 *
 * 권장 정책
 * ------------------------------------------------------------------------------------------
 * - 진화 가능 상태에서는 업그레이드 버튼보다 진화 버튼을 더 강조한다.
 * - 최대 레벨 또는 진화 대기 상태에서는 업그레이드 버튼을 비활성화한다.
 * - 진화는 세부정보 팝업이 아니라 업그레이드 팝업에서 처리한다.
 *
 * ==========================================================================================
 * 4. TurretDetailPopup 역할
 * ==========================================================================================
 *
 * TurretDetailPopup은 읽기 전용 상세 정보 화면으로 사용한다.
 *
 * 표시 정보
 * ------------------------------------------------------------------------------------------
 * - 현재 공격력
 * - 현재 사거리
 * - 발사 간격
 * - 탄속
 * - 투사체 수
 * - 관통 횟수
 * - 적용 중인 상태이상 또는 특수 공격 정보
 * - 탑승 중인 엔지니어 목록
 * - 엔지니어별 버프 수치
 *
 * 기본 정책
 * ------------------------------------------------------------------------------------------
 * - 이 팝업에서는 업그레이드와 진화를 직접 실행하지 않는다.
 * - 정보 확인과 엔지니어 상태 확인에 집중한다.
 *
 * ==========================================================================================
 * 5. TurretSkillPopup 역할
 * ==========================================================================================
 *
 * TurretSkillPopup은 향후 터렛 스킬 시스템을 위한 전용 화면이다.
 *
 * 초기 정책
 * ------------------------------------------------------------------------------------------
 * - 스킬 시스템이 준비되기 전에는 버튼을 비활성화하거나 "준비 중"으로 표시한다.
 * - 선택 팝업에는 스킬 버튼 자리를 미리 확보해 UI 확장성을 유지한다.
 *
 * 향후 표시 후보
 * ------------------------------------------------------------------------------------------
 * - 액티브 스킬 이름
 * - 쿨타임
 * - 효과 설명
 * - 스킬 강화 비용
 * - 스킬 사용/자동 사용 설정
 *
 * ==========================================================================================
 * 6. 2026-06-30 작업 기록
 * ==========================================================================================
 *
 * 오늘 완료한 내용
 * ------------------------------------------------------------------------------------------
 * - 설치된 터렛 1번 클릭 시 사거리만 표시하고, 같은 터렛 2번 클릭 시 TurretSelectPopup을
 *   표시하는 기본 흐름을 연결했다.
 * - TurretSelectPopup에서 업그레이드, 세부정보, 스킬, 닫기 버튼을 상위 컨트롤러로
 *   라우팅하는 구조를 만들었다.
 * - TurretSelectPopup의 Level, DPS, FireRate는 TMP 템플릿의 "{}" 구간만 실제 값으로
 *   치환하도록 수정했다.
 * - TurretDefinitionSO.shortDescription을 추가했고, TurretSelectPopup의 Note가 이 값을
 *   표시하도록 연결했다.
 * - 설명글은 TMP Rich Text를 그대로 사용하므로 "<nobr>단어</nobr>" 형태의 태그를
 *   입력해도 코드에서 가공하지 않는다.
 * - TurretUpgradePopupUI는 현재/다음 수치, 변화량, 업그레이드 비용, 업그레이드 버튼,
 *   진화 팝업 이동 버튼의 1차 구조를 가진다.
 * - 업그레이드/진화 비용 슬롯은 ResourceCost와 InventorySystem 메타데이터를 기준으로
 *   재화 이름, 수량, 이미지를 표시하는 방향으로 정리했다.
 * - TurretEvolutionPopupUI를 추가했고, UpgradePopup의 Evolution 버튼에서 여는 구조를
 *   준비했다.
 * - TurretDetailPopupUI와 TurretSkillPopupUI는 1차 표시/준비 중 상태까지 연결했다.
 * - 머지 이후 BottomBar 터렛 드래그 배치와 카메라 드래그 이동이 함께 멈춘 문제를
 *   진단했다.
 * - UI RaycastAll은 TurretPlacementSlot_Button과 CameraControlCanvas/Panel을 정상 감지했지만,
 *   TurretPlacementSlotUI와 CameraTouchHandler의 Pointer/Drag 콜백은 호출되지 않았다.
 * - InputSystemUIInputModule의 액션 에셋 참조가 현재 프로젝트의 InputSystem_Actions GUID와
 *   어긋난 정황을 확인했고, GUID를 현재 에셋으로 복구했다.
 * - GUID 복구 후에도 런타임 포인터 콜백이 들어오지 않아, EventSystemDebugger에
 *   구식 Input 기반 Legacy Pointer Bridge를 추가했다.
 * - Legacy Pointer Bridge는 RaycastAll 최상단 UI 오브젝트에 PointerDown, BeginDrag, Drag,
 *   EndDrag, PointerUp, Click 이벤트를 ExecuteEvents로 직접 전달한다.
 * - 브릿지 적용 후 BottomBar 터렛 드래그 설치와 카메라 드래그 이동이 다시 동작함을
 *   플레이 모드에서 확인했다.
 *
 * 남은 리스크
 * ------------------------------------------------------------------------------------------
 * - 일부 UI 참조는 아직 계층 이름 기반 자동 연결에 의존한다.
 * - 실제 Canvas > Turret UI 하위 오브젝트와 Inspector 참조가 전부 일치하는지 더
 *   확인해야 한다.
 * - 기존 옛날 터렛 UI가 남아 있으면 더블클릭 또는 버튼 입력과 충돌할 수 있다.
 * - EvolutionPopup의 이미지, 재화 슬롯, 버튼 활성 조건은 플레이 모드 검증이 더 필요하다.
 * - 모든 팝업의 Back/X/바깥 클릭 닫힘 정책을 같은 기준으로 정리해야 한다.
 * - Legacy Pointer Bridge는 현재 입력 복구용 호환 레이어다. 추후 InputSystemUIInputModule
 *   기본 콜백 경로가 정상화되면 같은 입력이 중복 전달되지 않는지 먼저 확인해야 한다.
 * - EventSystemDebugger가 Main 씬에서 비활성화되면 현재 브릿지 입력도 같이 꺼진다.
